"""实验: jieba 默认分词 vs 加载金融词典后的分词
目的: 直观看到自定义词典的价值
"""
import jieba

# 我们项目里会遇到的典型金融术语
test_cases = [
    "招商银行不良贷款率",
    "贵州茅台营业收入",
    "归属于上市公司股东的净利润",
    "基本每股收益",
    "加权平均净资产收益率",
    "扣除非经常性损益后的净利润",
    "资产负债率",
    "毛利率",
    "经营活动产生的现金流量净额",
    "宁德时代毛利率",
]

# ========== 实验 A: jieba 默认分词 ==========
print("=" * 70)
print("实验 A: jieba 默认分词(没有金融词典)")
print("=" * 70)
for text in test_cases:
    tokens = list(jieba.cut(text))
    print(f"  {text:30s} → {tokens}")

# ========== 实验 B: 加载金融词典后 ==========
print("\n" + "=" * 70)
print("实验 B: 加载金融词典后的分词")
print("=" * 70)

# 金融术语白名单(这些必须作为整体词)
FINANCIAL_TERMS = [
    # 贷款类
    "不良贷款率", "拨备覆盖率", "贷款拨备率",
    # 利润类
    "营业收入", "营业利润", "净利润", "归母净利润",
    "扣非净利润", "毛利率", "净利率",
    # 股东/股权
    "归属于上市公司股东的净利润",
    "每股收益", "基本每股收益", "稀释每股收益",
    # 资产类
    "总资产", "净资产", "资产负债率", "加权平均净资产收益率",
    # 现金流
    "经营活动产生的现金流量净额",
    "投资活动产生的现金流量净额",
    "筹资活动产生的现金流量净额",
    # 公司名(保证不被切碎)
    "贵州茅台", "招商银行", "宁德时代",
    # 业务术语
    "扣除非经常性损益", "研发投入", "研发费用",
    "关联交易", "关联方",
]

for term in FINANCIAL_TERMS:
    jieba.add_word(term)

for text in test_cases:
    tokens = list(jieba.cut(text))
    print(f"  {text:30s} → {tokens}")

# ========== 实验 C: 看看对 BGE 查询的影响 ==========
print("\n" + "=" * 70)
print("实验 C: 同一个查询的两种切法, 对 BM25 打分的隐含影响")
print("=" * 70)

query = "招商银行 2025 年的不良贷款率是多少"
print(f"查询: {query}\n")

jieba.del_word("不良贷款率")  # 临时移除自定义词
print(f"  默认切:  {list(jieba.cut(query))}")

jieba.add_word("不良贷款率")  # 恢复
print(f"  词典切:  {list(jieba.cut(query))}")

print("""
🔍 观察:
  默认切法下, "不良贷款率"被切成 ["不良", "贷款率"]
  BM25 会去找同时包含这 2 个词的文档,
  但如果文档里写的是"不良贷款率"(一个整体),
  jieba 对文档也会切成 ["不良", "贷款率"], 所以还能匹配上。

  真正的问题在于 IDF 权重: 
    "不良"单独看是常见词(稠密出现) → IDF 低
    "不良贷款率"整体看是罕见专业术语 → IDF 极高

  切碎了 → 失去了"罕见术语"的高权重优势。
  保持整体 → BM25 能精准打分。
""")