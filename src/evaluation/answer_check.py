from __future__ import annotations


# 拒答 pattern 集. 来源: Day 9 U5 10 条拒答样本归纳
# 每条都是完整 4+ 字短语, 不会被日常介词意外切中
REFUSAL_PATTERNS: tuple[str, ...] = (
    "根据已有资料无法回答",
    "无法回答",
    "未提供",
    "未披露",
    "未提及",
    "资料不足",
    "参考资料中未",
    "参考资料未",
)


def is_refusal(answer: str) -> tuple[bool, str | None]:
    """检测 LLM 答案是否为拒答态.

    拒答态 = LLM 明说"我无法回答/资料不足", 是一种诚实表达.
    不是"答错", 不是"答不全". 见本模块 FN-2.

    Args:
        answer: LLM 生成的完整答案字符串

    Returns:
        (True, 命中的 pattern) 如果命中任一 REFUSAL_PATTERNS
        (False, None) 如果没命中

    Example:
        >>> is_refusal("根据已有资料无法回答此问题, 参考资料中提到了...")
        (True, '根据已有资料无法回答')
        >>> is_refusal("招行 2025 年不良贷款率为 0.93%")
        (False, None)
    """
    if not isinstance(answer, str):
        # 防御性: LLM 调用失败时可能返回 None
        return False, None

    for pattern in REFUSAL_PATTERNS:
        if pattern in answer:
            return True, pattern
    return False, None


# ==============================================================================
# 自测 (跑: python -m src.evaluation.answer_check)
# ==============================================================================

def _self_test() -> None:
    """最小自测: 4 正例 (真实拒答) + 4 反例 (真实作答).

    正例来源: Day 9 U5 拒答样本模式 + U3 Day 9 答案模式
    反例: 模拟 U3/U6 正确作答的字符串
    """
    # 正例: 应该被判为拒答 (is_refusal 返回 True)
    positives = [
        # Day 9 U5 典型拒答
        "根据已有资料无法回答此问题, 参考资料中提到了海外业务和地缘政治风险的"
        "应对措施, 但未提供具体营收数据",
        # Day 9 U3 典型半拒答 (集团口径部分)
        "参考资料中提供了国电南自部分子公司2024年的净利润数据, 例如南京国电"
        "南自自动化有限公司为427,419,723.66元, 但未提供国电南自母公司或整个"
        "集团合并口径的2024年度净利润总额",
        # 短拒答
        "无法回答此问题",
        # 另一种常见表达
        "参考资料中未找到相关信息",
    ]

    # 反例: 应该被判为作答 (is_refusal 返回 False)
    negatives = [
        # Day 9 U6 正确作答样例
        "宁德时代 2025 年营业收入为 423,701,834 千元, 同比增长 17.04%",
        # 招行正确作答样例
        "招商银行 2025 年度不良贷款率为 0.93%",
        # 含金融术语但不拒答
        "毛利率提升至 25.3%, 主要由于成本优化",
        # 纯数字答案
        "5.82 亿元",
    ]

    failures = []

    for ans in positives:
        hit, pattern = is_refusal(ans)
        if not hit:
            failures.append(f"[FN] 应判拒答但未命中: {ans[:40]}...")
        else:
            print(f"  ✅ [正例] 命中 pattern '{pattern}': {ans[:40]}...")

    for ans in negatives:
        hit, pattern = is_refusal(ans)
        if hit:
            failures.append(
                f"[FP] 应判作答但误命中 pattern '{pattern}': {ans[:40]}..."
            )
        else:
            print(f"  ✅ [反例] 未命中: {ans[:40]}...")

    print()
    if failures:
        print(f"❌ 自测失败 ({len(failures)} 项):")
        for f in failures:
            print(f"  {f}")
        raise SystemExit(1)
    else:
        print(f"✅ 自测全过 ({len(positives)} 正例 + {len(negatives)} 反例)")


if __name__ == "__main__":
    _self_test()